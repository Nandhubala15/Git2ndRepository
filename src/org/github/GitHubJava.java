package org.github;

public class GitHubJava {

	public static void main(String[] args) {

		for (int i = 5; i >= 1; i--) {
			for (int j = 1; j <= i; j++) {
				System.out.print(i + " ");

			}
			System.out.println();
		}
		
		
		for (int k = 5; k >=1 ; k--) {
			for (int j = 1; j <=k; j++) {
				System.out.print(j + " ");
				
			}System.out.println();
			
		}

	}

}
